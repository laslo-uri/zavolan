IMG_SPLIT = re.compile(r"(\!\[\]\([^)]+\))")


def replace_outside_images(text: str, old: str, new: str) -> str:
    parts = IMG_SPLIT.split(text)
    for i in range(0, len(parts), 2):
        parts[i] = parts[i].replace(old, new)
    return "".join(parts)


