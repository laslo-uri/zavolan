def strip_page_only_headings(text: str) -> str:
    """Remove ### N headings that are OCR page markers (standalone digit)."""
    return re.sub(r"^###\s+\d+\s*\n", "\n", text, flags=re.MULTILINE)


def apply_normalizations(text: str) -> str:
    """Serbian Latin + OCR typo fixes; never modifies text inside ![](...) segments."""
    for old, new in REPLACEMENTS:
        text = replace_outside_images(text, old, new)
    for old, new in FIXUPS:
        text = replace_outside_images(text, old, new)
    text = strip_page_only_headings(text)
    return text


def main() -> None:
    import sys

    path = Path(sys.argv[1]) if len(sys.argv) > 1 else MD
    text = path.read_text(encoding="utf-8")
    text = apply_normalizations(text)
    path.write_text(text, encoding="utf-8")
    print("Wrote", path)


if __name__ == "__main__":
    main()
